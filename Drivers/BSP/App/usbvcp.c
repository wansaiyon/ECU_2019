void vUSBVCP_Proc(uint8_t* Buf, uint32_t *Len)
{
	if(Buf[0]==0x02 && Buf[1]==0xa8)
	{
		HAL_GPIO_WritePin(LED3_GPIO_Port,LED3_Pin,GPIO_PIN_RESET);
		CDC_Transmit_FS((uint8_t*)"\r\nUSBCDC:LED3 ON!\r\n",sizeof("\r\nUSBCDC:LED3 ON!\r\n"));
	}
	else if(Buf[0]==0x79 && Buf[1]==0xc3)
	{
		HAL_GPIO_WritePin(LED3_GPIO_Port,LED3_Pin,GPIO_PIN_SET);
		CDC_Transmit_FS((uint8_t*)"\r\nUSBCDC:LED3 OFF!\r\n",sizeof("\r\nUSBCDC:LED3 OFF!\r\n"));
	}
}








